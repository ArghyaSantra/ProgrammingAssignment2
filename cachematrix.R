makeVector <- function(x = numeric()) {
        m <- NULL
        set <- function(y) {
                x <<- y    ##x is a variable which stores value of y
                m <<- NULL ##m stores Null
        }
        get <- function() x ##get stores valuegenerated by function()x
        setmean <- function(mean) m <<- mean
        getmean <- function() m
        list(set = set, get = get,
             setmean = setmean,
             getmean = getmean)
}


