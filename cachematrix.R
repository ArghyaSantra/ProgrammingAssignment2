cachemean <- function(x, ...) {
        m <- x$getmean()##m stores the value getmean attribute
        if(!is.null(m)) {
                message("getting cached data")
                return(m)
        }
        data <- x$get()
        m <- mean(data, ...)
        x$setmean(m)
        m
}
